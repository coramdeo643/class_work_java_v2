package ch03;

public class IfTest2 {
    // entry
    public static void main(String[] args) {

        int age = 10;
        // if-else; 반드시 하나는 수행이 된다
        if (age >= 20) {
            System.out.println("조금 아저씨네유");
        } else {
            System.out.println("아직 조금 어리네유");
        }


    } // end of main
} // end of class

