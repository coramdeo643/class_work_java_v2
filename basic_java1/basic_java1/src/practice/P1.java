public class P1 {
    public static void main(String[] args) {

        for (int b = 1; b < 6; b++) {
            for (int a = b; a > 0; a--) {
                System.out.print(" * ");
            } // end of for
            System.out.println(" ");
        } // end of for

    } // end of main
} // end of class
